package col106.assignment6;

public class DualGraphNode {
    int i1;
    int j1;
    int i2;
    int j2;
    int key;

    public DualGraphNode(int i1, int j1, int i2, int j2, int key){
        this.i1 = i1;
        this.j1 = j1;
        this.i2 = i2;
        this.j2 = j2;
        this.key = key;
    }
}